def get_grade(score: int):
    """Возвращает (грейд, описание, имя картинки) по набранным баллам."""
    percent = score * 100 / MAX_SCORE
    _, title, desc, img = GRADES[0]
    for threshold, t, d, i in GRADES:
        if percent >= threshold:
            title, desc, img = t, d, i
    return title, desc, img
